#define SQSUM_MAX_VAL       (100)
#define SQSUM_TRHESHOLD     (SQSUM_MAX_VAL + 1)
// Answer: 


/*******************************************************************************\
* Static Variables
\*******************************************************************************/


/*******************************************************************************\
* Static Functions
\*******************************************************************************/
static U4 u4_s_SqSum_GetDiff(void);
static U4 u4_s_SqSum_GetSumSq(void);
static U4 u4_s_SqSum_GetSqSum(void);


/*******************************************************************************\
* Function Definitions
\*******************************************************************************/


/*****************************************************************************************************************************************************************\
* Function name     : vd_g_SqSum_Main
* Called by         : main
* Argument(s)       : void
* Return            : void
* Description       : Entry Point for Euler Project 6 SqSum
\*****************************************************************************************************************************************************************/
void vd_g_SqSum_Main(void)
{
    U4 u4_t_ans;

    cout << "Find the difference between the square of the sum and the sum of the sqaures..\n\n";

    u4_t_ans = u4_s_SqSum_GetDiff();

    cout << "Answer:\t" << u4_t_ans << "\n\n";
}


/*****************************************************************************************************************************************************************\
* Function name     : u4_s_SqSum_GetDiff
* Called by         : vd_g_SqSum_Main
* Argument(s)       : void
* Return            : U4
* Description       : GetDiff
\*****************************************************************************************************************************************************************/
static U4 u4_s_SqSum_GetDiff(void)
{
    U4 u4_t_sumsq;
    U4 u4_t_sqsum;
    U4 u4_t_diff;

    u4_t_sumsq = u4_s_SqSum_GetSumSq();
    u4_t_sqsum = u4_s_SqSum_GetSqSum();

    u4_t_diff  = u4_t_sqsum - u4_t_sumsq;

    return(u4_t_diff);
}


/*****************************************************************************************************************************************************************\
* Function name     : u4_s_SqSum_GetSumSq
* Called by         : u4_s_SqSum_GetDiff
* Argument(s)       : void
* Return            : U4
* Description       : GetDiff
\*****************************************************************************************************************************************************************/
static U4 u4_s_SqSum_GetSumSq(void)
{
    U4 u4_t_val;
    U4 u4_t_sumsq;
    
    u4_t_sumsq = (U4)0;

    for(u4_t_val = (U4)1; u4_t_val < (U4)SQSUM_TRHESHOLD; u4_t_val++)
    {
        u4_t_sumsq += (u4_t_val * u4_t_val);
    }

    return(u4_t_sumsq);
}


/*****************************************************************************************************************************************************************\
* Function name     : u4_s_SqSum_GetSqSum
* Called by         : u4_s_SqSum_GetDiff
* Argument(s)       : void
* Return            : U4
* Description       : GetDiff
\*****************************************************************************************************************************************************************/
static U4 u4_s_SqSum_GetSqSum(void)
{
    U4 u4_t_val;
    U4 u4_t_sqsum;

    u4_t_sqsum = (U4)0;

    for(u4_t_val = (U4)1; u4_t_val < (U4)SQSUM_TRHESHOLD; u4_t_val++)
    {
        u4_t_sqsum += u4_t_val;
    }

    u4_t_sqsum *= u4_t_sqsum;

    return(u4_t_sqsum);
}
